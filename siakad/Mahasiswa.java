public class Mahasiswa {
    private int nim;
    private String nama;

    public void setNim(int nim) {
        this.nim = nim;
    }

    public int getNim() {
        return this.nim;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNama() {
        return this.nama;
    }
}